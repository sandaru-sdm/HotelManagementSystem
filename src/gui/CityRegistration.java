    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter City Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `city` WHERE `name`='" + name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "City Already Exists.", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `city` (`name`) VALUES ('" + name + "')");
                    loadCities();
                     if (Customerregistration) {
                        this.cr.loadCities();
                    } else if (Userregistration) {
                        this.ur.loadCities();
                    }
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                    JOptionPane.showMessageDialog(this, "City Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cityName = jTextField1.getText().trim();
        if (cityName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter City Name.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MySQL.iud("UPDATE `city` SET `name`='" + cityName + "' WHERE `id`='" + city_ID + "'");
            jTextField1.setText("");
            loadCities();
            JOptionPane.showMessageDialog(this, "City Updated Successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

