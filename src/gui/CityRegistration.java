    public void loadCities() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `city`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
            jTable1.setModel(dtm);
            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadCities(String text) {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `city` WHERE `city`.`name` "
                    + "LIKE '" + text + "%' ORDER BY `city`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                dtm.addRow(v);
            }
            jTable1.setModel(dtm);
            jButton1.setEnabled(true);
            jButton2.setEnabled(false);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

