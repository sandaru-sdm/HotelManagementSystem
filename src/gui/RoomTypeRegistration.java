    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int r = jTable1.getSelectedRow();
        if (evt.getClickCount() == 1) {
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select Room Type.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                RoomTypeID = jTable1.getValueAt(r, 0).toString();
                String roomType = jTable1.getValueAt(r, 1).toString();
                String price = jTable1.getValueAt(r, 2).toString();
                jTextField1.setText(roomType);
                jTextField3.setText("" + price);
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

