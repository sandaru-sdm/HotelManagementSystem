    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        String price = jTextField3.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Room Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]-[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Price.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `room_type` WHERE `name`='" + name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Room Type Already Exists.", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `room_type` (`name`,`price`) VALUES ('" + name + "','" + price + "')");
                    loadRoomTypes();
                    jTextField1.setText("");
                    jTextField1.grabFocus();
                    jTextField3.setText("");
                    JOptionPane.showMessageDialog(this, "Room Type Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.rr.loadRoomTypes();
                    RoomRegistration room = new RoomRegistration();
                    room.loadRooms();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String roomType = jTextField1.getText().trim();
            String price = jTextField3.getText().trim();
            if (roomType.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Room Type.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!Pattern.compile("([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]-[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(price).matches()) {
                JOptionPane.showMessageDialog(this, "Invalid Price.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                ResultSet rs = MySQL.search("SELECT * FROM `room_type` WHERE `name`='" + roomType + "'");
                if (rs.next()) {
                    jTextField1.setEditable(false);
                    MySQL.iud("UPDATE `room_type` SET `name`='" + roomType + "',`price`='" + price + "' WHERE `id`='" + RoomTypeID + "'");
                    jTextField1.setText("");
                    jTextField3.setText("");
                    loadRoomTypes();
                    RoomRegistration rooms = new RoomRegistration();
                    rooms.loadRooms();
                    JOptionPane.showMessageDialog(this, "Room Type Updated Successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    rooms.loadRooms();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

